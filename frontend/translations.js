window.translations = translations;

window.t = function(key, lang = localStorage.getItem('app_lang') || 'ar') {
    if (translations[lang] && translations[lang][key]) {
        return translations[lang][key];
    }
    // Fallback to English
    if (translations['en'] && translations['en'][key]) {
        return translations['en'][key];
    }
    // Fallback to key
    return key;
};

window.formatNumber = function(num, lang = localStorage.getItem('app_lang') || 'ar') {
    try {
        return new Intl.NumberFormat(lang).format(num);
    } catch(e) {
        return num;
    }
};

window.formatCurrency = function(numStr, lang = localStorage.getItem('app_lang') || 'ar') {
    try {
        const val = parseFloat(numStr.replace(/[^0-9.]/g, ''));
        if (isNaN(val)) return numStr;
        const curMap = {
            ar: { code: 'EGP', symbol: 'ج.م' },
            en: { code: 'USD', symbol: '$' },
            fr: { code: 'EUR', symbol: '€' },
            de: { code: 'EUR', symbol: '€' },
            es: { code: 'EUR', symbol: '€' }
        };
        const c = curMap[lang] || { code: 'USD', symbol: '$' };
        return new Intl.NumberFormat(lang, { style: 'currency', currency: c.code }).format(val);
    } catch(e) {
        return numStr;
    }
};

window.formatDate = function(dateStr, lang = localStorage.getItem('app_lang') || 'ar') {
    try {
        const d = new Date(dateStr);
        if (isNaN(d.getTime())) return dateStr;
        return new Intl.DateTimeFormat(lang, { dateStyle: 'medium' }).format(d);
    } catch(e) {
        return dateStr;
    }
};
